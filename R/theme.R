
#' Set theme for \code{mplot3} functions
#'
theme_blackgrid <- function(bg = "#000000",
                            plot.bg = NA,
                            fg.col = "#ffffff",
                            pch = 16,
                            cex = 1.2,
                            lwd = 2,
                            # box --
                            bty = "n",
                            box.col = fg.col,
                            box.alpha = 1,
                            box.lty = 1,
                            box.lwd = .5,
                            # grid --
                            grid = TRUE,
                            grid.nx = NULL,
                            grid.ny = NULL,
                            grid.col = fg.col,
                            grid.alpha = .2,
                            grid.lty = 1,
                            grid.lwd = 1,
                            # axes --
                            axes = TRUE,
                            axes.col = NA,
                            tick.col = NA,
                            tick.alpha = 0,
                            tick.labels.col = fg.col,
                            x.axis.side = 1,
                            y.axis.side = 2,
                            labs.col = fg.col,
                            # zerolines --
                            zerolines = TRUE,
                            zerolines.col = fg.col,
                            zerolines.alpha = .5,
                            zerolines.lty = 1,
                            zerolines.lwd = 1,
                            # title --
                            main.line = .5,
                            main.adj = 0,
                            main.font = 1,
                            main.col = fg.col,
                            font.family = "") {

  list(bg = bg,
       plot.bg = plot.bg,
       fg.col = fg.col,
       pch = pch,
       cex = cex,
       lwd = lwd,
       # box --
       bty = bty,
       box.col = box.col,
       box.alpha = box.alpha,
       box.lty = box.lty,
       box.lwd = box.lwd,
       # grid --
       grid = grid,
       grid.nx = grid.nx,
       grid.ny = grid.ny,
       grid.col = grid.col,
       grid.alpha = grid.alpha,
       grid.lty = grid.lty,
       grid.lwd = grid.lwd,
       # axes --
       axes = axes,
       axes.col = axes.col,
       tick.col = tick.col,
       tick.alpha = tick.alpha,
       tick.labels.col = tick.labels.col,
       x.axis.side = x.axis.side,
       y.axis.side = y.axis.side,
       labs.col = labs.col,
       # zerolines --
       zerolines = zerolines,
       zerolines.col = zerolines.col,
       zerolines.alpha = zerolines.alpha,
       zerolines.lty = zerolines.lty,
       zerolines.lwd = zerolines.lwd,
       # title --
       main.line = main.line,
       main.adj = main.adj,
       main.font = main.font,
       main.col = main.col,
       font.family = font.family)

} # rtemis::theme_blackgrid

